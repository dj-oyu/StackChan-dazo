#include <stdint.h>

#define _U 0x01
#define _L 0x02
#define _N 0x04
#define _S 0x08
#define _P 0x10
#define _C 0x20
#define _X 0x40
#define _B 0x80

const uint8_t _ctype_[257] = {
    [1 + 0 ... 1 + 8]     = _C,
    [1 + 9]               = _C | _S | _B,
    [1 + 10 ... 1 + 13]   = _C | _S,
    [1 + 14 ... 1 + 31]   = _C,
    [1 + ' ']             = _S | _B,
    [1 + '!' ... 1 + '/'] = _P,
    [1 + '0' ... 1 + '9'] = _N | _X,
    [1 + ':' ... 1 + '@'] = _P,
    [1 + 'A' ... 1 + 'F'] = _U | _X,
    [1 + 'G' ... 1 + 'Z'] = _U,
    [1 + '[' ... 1 + '`'] = _P,
    [1 + 'a' ... 1 + 'f'] = _L | _X,
    [1 + 'g' ... 1 + 'z'] = _L,
    [1 + '{' ... 1 + '~'] = _P,
    [1 + 127]             = _C,
};
